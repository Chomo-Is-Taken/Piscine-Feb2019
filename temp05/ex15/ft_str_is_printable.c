int	ft_str_is(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] > 32 && str[i] < 127)
			i++;
		else
			return (0);
	}
	if (i == 0)
		return (1);
	return (1);
}
